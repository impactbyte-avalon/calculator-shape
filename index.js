function calculateSquareArea(a) {
	return a * a
}

function calculateSquarePerimeter(a) {
	return (a + a)* 2
}

function calculateCircleArea(a) {
	// body...
	return (a * a) * Math.PI
}

function calculateCircleCircumference(a, b) {
	// body...
	return Math.PI * b
}

function calculateCubeArea(a) {
	// body...
	return a * a 
}

function calculateCubeVolume(a) {
	// body...
	return a * a * a
}

function calculateTubeArea(a, b) {
	// body...
	return 2 * Math.PI * a * b
}

function calculateTubeVolume(a, b) {
	// body...
	return Math.PI * ((a / 2) ^ 2 ) * b
}